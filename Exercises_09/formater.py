
# Functions
def convert_upper(text):
    return text.upper()
def convert_lower(text):
    return text.lower()
def convert_capitalize(text):
    return text.capitalize()
def convert_swapcase(text):
    return text.swapcase()

# Main code
print(convert_upper("krish"))
print(convert_lower("Krish"))
print(convert_capitalize("kRiSh"))
print(convert_swapcase("kRiSh"))